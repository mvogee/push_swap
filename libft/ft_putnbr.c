#include "libft.h"

void	ft_putnbr(int n)
{
	long	tmp;

	tmp = (long)n;
	if (tmp < 0)
	{
		ft_putchar('-');
		tmp *= -1;
	}
	if (tmp / 10)
		ft_putnbr(tmp / 10);
	ft_putchar(tmp % 10 + '0');
}
